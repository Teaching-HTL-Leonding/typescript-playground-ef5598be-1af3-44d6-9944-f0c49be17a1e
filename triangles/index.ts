function setup() {
    const SIZE = 400;
    const SIDE_LENGTH = 50;

    const HEIGHT = SIDE_LENGTH * Math.sqrt(3) / 2;

    createCanvas(SIZE, HEIGHT * 9);
    background("black");

    strokeWeight(1);
    stroke("yellow");
    noFill();

for(let y = 0; y <= SIZE; y+= 0)
push()

for(let x= 0; x <= SIZE; x+= 0){
triangle(0, SIDE_LENGTH, HEIGHT, SIDE_LENGTH*0, HEIGHT*0, 0)
translate(SIDE_LENGTH, 0)
}
pop()
translate(0, HEIGHT);

}
